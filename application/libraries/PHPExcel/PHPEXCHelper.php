<?php
function NextCol($var1)
{	
	if($var1=='A') return 'B';
	else  if($var1=='B') return 'C';
	else  if($var1=='C') return 'D';
	else  if($var1=='D') return 'E';
	else  if($var1=='E') return 'F'	;
	else  if($var1=='F') return 'G'	;
	else  if($var1=='G') return 'H';
	else  if($var1=='H') return 'I';
	else  if($var1=='I') return 'J';
	else  if($var1=='J') return 'K';
	else  if($var1=='K') return 'L';
	else  if($var1=='L') return 'M';
	else  if($var1=='M') return 'N';
	else  if($var1=='N') return 'O';
	else  if($var1=='O') return 'P';
	else  if($var1=='P') return 'Q';
	else  if($var1=='Q') return 'R';
	else  if($var1=='R') return 'S';
	else  if($var1=='S') return 'T';
	else  if($var1=='T') return 'U';
	else  if($var1=='U') return 'V';
	else  if($var1=='V') return 'W';
	else  if($var1=='W') return 'X';
	else  if($var1=='X') return 'Y';
	else  if($var1=='Y') return 'Z';
	else  if($var1=='Z') return 'AA';
	else return '';
}


